obj_list = [joker, dog, elephant, horse, monkey, horse1, flight, jeep]

pause = False

# Ignore Ctrl+c and Ctrl+Z
signal.signal(signal.SIGINT, signal.SIG_IGN)

def main(stdscr):
    for scroll_obj in obj_list:
        global pause
        curses.curs_set(0)        # Hide cursor
        stdscr.nodelay(True)      # Non-blocking input
        stdscr.clear()

        max_y, max_x = stdscr.getmaxyx()
        height = len(scroll_obj)
        width = max(len(line) for line in scroll_obj)

        y_pos = max_y // 2 - height // 2
        x_pos = max_x

        while x_pos > -width:
            if pause:
                key = stdscr.getch()
                if key == ord(' '):
                    if pause:
                        pause = False
                continue

            stdscr.clear()
            for i, line in enumerate(scroll_obj):
                y = y_pos + i
                if 0 <= y < max_y and x_pos < max_x:
                    visible_part = line[max(0, -x_pos):max_x - x_pos]
                    if visible_part:
                        stdscr.addstr(y, max(0, x_pos), visible_part)

            stdscr.refresh()
            time.sleep(0.05)
            x_pos -= 1

            key = stdscr.getch()
            if key == ord(' '):
                if not pause:
                    pause = True

curses.wrapper(main)
